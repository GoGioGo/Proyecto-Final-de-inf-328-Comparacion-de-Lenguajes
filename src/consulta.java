    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String a, b;
        Cluster cluster;
        Session session;
        cluster = Cluster.builder().addContactPoint("127.0.0.1").build();
        session = cluster.connect("proyecto_bonos");
        ResultSet rs = session.execute("select ci, fnac from persona");
        Iterator<Row> it = rs.iterator();
        while (it.hasNext()) {
            Row ro = it.next();
            if (ci.getText().equals(String.valueOf(ro.getInt("ci")))) {
                formulario f = new formulario();
                f.setVisible(true);
                f.cargarPA(ro.getInt("ci"));
                dispose();
            }
        }
        ResultSet r = session.execute("select ci from aporte;");
        Iterator<Row> ite = r.iterator();
        while (ite.hasNext()) {
            Row ro = ite.next();
            if (ci.getText().equals(String.valueOf(ro.getInt("ci")))) {
                aportante ap = new aportante();
                ap.setVisible(true);
                ap.cargarPA(Integer.parseInt(ci.getText()));
                dispose();
            }
        }

        ResultSet res = session.execute("select ci from menor;");
        Iterator<Row> itera = res.iterator();
        while (itera.hasNext()) {
            Row ro = itera.next();
            if (ci.getText().equals(String.valueOf(ro.getInt("ci")))) {
                edad e = new edad();
                e.setVisible(true);
                e.cargarPA(Integer.parseInt(ci.getText()));
                dispose();
            }
        }

        men.setText("No existe registro de esa C.I. vuelva a intentar...");
        ci.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

