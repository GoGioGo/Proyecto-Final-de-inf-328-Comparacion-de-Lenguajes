        Session session;
        cluster = Cluster.builder().addContactPoint("127.0.0.1").build();
        session = cluster.connect("proyecto_bonos");
        session.execute("insert into persona(ci, nom, fnac, edad, fin) values (" + Integer.parseInt(ci.getText()) + ",'" + nom.getText() + "','" + fec.getText() + "'," + Integer.parseInt(eda.getText()) + ",'" + fin.getText() + "');");
